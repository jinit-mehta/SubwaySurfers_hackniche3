const contractAddress = '0xd8b934580fcE35a11B58C6D73aDeE468a2833fa8'; // Replace with your contract's address

export const connectWallet = async () => {
  if (typeof window.ethereum !== 'undefined') {
    try {
      const provider = new ethers.providers.Web3Provider(window.ethereum);
      await provider.send('eth_requestAccounts', []);
      const signer = provider.getSigner();
      const contract = new ethers.Contract(contractAddress, contractABI, signer);
      return { provider, signer, contract };
    } catch (error) {
      console.error('Error connecting to wallet:', error);
      throw error;
    }
  } else {
    throw new Error('Ethereum object not found. Please install MetaMask or a compatible wallet.');
  }
};

export const getGreeting = async (contract) => {
  try {
    const greeting = await contract.getGreeting();
    return greeting;
  } catch (error) {
    console.error('Error fetching greeting:', error);
    throw error;
  }
};

export const setGreeting = async (contract, newGreeting) => {
  try {
    const transaction = await contract.setGreeting(newGreeting);
    await transaction.wait();
    return transaction;
  } catch (error) {
    console.error('Error setting greeting:', error);
    throw error;
  }
};